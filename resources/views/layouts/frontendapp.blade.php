    <script>
        const minSlider = document.getElementById('min-slider');
        const maxSlider = document.getElementById('max-slider');
        const rangeBetween = document.getElementById('range-between');
        const minValueText = document.getElementById('min-value');
        const maxValueText = document.getElementById('max-value');
        const sliderContainer = document.querySelector('.slider-container');

        function updateRange() {
            let minVal = parseInt(minSlider.value);
            let maxVal = parseInt(maxSlider.value);

            if (minVal > maxVal) {
                [minVal, maxVal] = [maxVal, minVal]; // Swap values
            }

            minValueText.textContent = minVal;
            maxValueText.textContent = maxVal;

            const range = maxSlider.max - minSlider.min;
            const left = ((minVal - minSlider.min) / range) * 100;
            const right = ((maxVal - minSlider.min) / range) * 100;

            rangeBetween.style.left = left + '%';
            rangeBetween.style.width = (right - left) + '%';
        }

        minSlider.addEventListener('input', updateRange);
        maxSlider.addEventListener('input', updateRange);

        updateRange(); // Initial update
    </script>
